def add(input1, input2):
    output_add = input1 + input2
    return output_add
def sub(input1, input2):
    output_add = input1 - input2
    return output_add
def mul(input1, input2):
    output_add = input1 * input2
    return output_add
def div(input1, input2):
    output_add = input1 / input2
    return float(output_add)